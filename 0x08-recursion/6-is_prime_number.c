#include <stdio.h>

/**
 * is_prime_number - prints 1 if print number and 0 otherwise
 * @n: the number given
 * Return: 1 if prime and 0 otherwise
 */
int is_prime_number(int n)
{
	int num = 0;

	if (num >= 0 && num < 0)
	{
		if (n % num != 0)
			return (1);
		else
			return (0);
		num++;
	}
	return (0);
}
